print("Para calcular a área da sala precisamos dos valores da profundidade e largura")
pronfundidade = float(input("Qual é a profundidade da sala? (em metros): "))
largura = float(input("Qual é a largura da sala? (em metros): "))

area = pronfundidade * largura

print(f"A área da sala é {area:.2f} metros quadrados (m²).")
